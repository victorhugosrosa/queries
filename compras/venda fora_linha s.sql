SELECT
	LINHA.[COD_LOJA]
	,LINHA.[COD_PRODUTO]
	,PROD.DESCRICAO
	,LINHA.[COD_SECAO]
	,LINHA.[COD_GRUPO]
	,LINHA.[FORA_LINHA]
	,LINHA.[ENVIA_PDV]
	,LINHA.[CLASSIF_PRODUTO_LOJA]
	,LINHA.[QTDE_PRODUTO_1ANO]
	,LINHA.[VALOR_TOTAL_1ANO]
FROM
	BI_LINHA_PRODUTOS AS LINHA left join BI_CAD_PRODUTO AS PROD ON (LINHA.COD_PRODUTO = PROD.COD_PRODUTO)
WHERE 1=1
	AND LINHA.COD_PRODUTO IN
	(
		SELECT  DISTINCT
		[COD_PRODUTO]
		FROM AX2009_INTEGRACAO.dbo.TAB_REFERENCIA_FORNECEDOR
		WHERE 1=1
		AND COD_FORNECEDOR = 61
		AND COD_PRODUTO IN (SELECT COD_PRODUTO FROM BI_CAD_PRODUTO WHERE COD_SECAO = 39)
	)
	AND LINHA.FORA_LINHA = 'S'


