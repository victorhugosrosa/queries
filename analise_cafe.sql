SELECT
	LP.COD_LOJA
	,L.NO_LOJA
	,LP.COD_PRODUTO
	,CP.DESCRICAO
	,CP.NO_SECAO
	,CP.NO_GRUPO
	,CP.NO_SUBGRUPO
	,LP.CLASSIF_PRODUTO_LOJA
	,dbo.fn_FormataVlr_Excel(ISNULL(LP.VLR_TOTAL_1ANO,0)) AS VLR_TOTAL_1ANO
	,dbo.fn_FormataVlr_Excel(ISNULL(LP.QTDE_PRODUTO_1ANO,0)) AS QTDE_PRODUTO_1ANO
	,dbo.fn_FormataVlr_Excel(LP.VLR_VENDA) AS VLR_VENDA
FROM
	BI.dbo.BI_LINHA_PRODUTOS AS LP INNER JOIN BI.dbo.BI_CAD_PRODUTO AS CP ON (LP.COD_PRODUTO = CP.COD_PRODUTO)
		INNER JOIN dbo.BI_CAD_LOJA2 AS L ON (LP.COD_LOJA = L.COD_LOJA)
WHERE 1 = 1
	AND LP.COD_LOJA <> 7
	AND LP.COD_PRODUTO IN (378093,378109,724869,378116,638326,638333,638340,114806,114813,307789,4404,4466,480154,11327,5128,387330,49566,49573,480642,480543,481434,481151,998445,998444,480994,480956,387309,387293,632041,577809,481625,481595,481571,387590,387323,999631,480925,480840,11778,480819,480659,481496,481465,999629,999632,11747,49603,999630,387316,622042,29025)
ORDER BY
	LP.COD_LOJA
	,LP.VLR_TOTAL_1ANO DESC
	
	
	