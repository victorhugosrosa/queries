SELECT
	LINHA.[COD_LOJA]
	,LINHA.[COD_PRODUTO]
	,LINHA.[COD_SECAO]
	,LINHA.[COD_GRUPO]
	,LINHA.[FORA_LINHA]
	,LINHA.[ENVIA_PDV]
	,LINHA.[CLASSIF_PRODUTO_LOJA]
	,LINHA.[QTDE_PRODUTO_1ANO]
	,LINHA.[VALOR_TOTAL_1ANO]
	,MOV.[QTD_ESTOQUE]
FROM
	[BI].[dbo].[BI_LINHA_PRODUTOS] AS LINHA LEFT JOIN [BI].[dbo].[BI_TESTE_MOVIMENTO_PRODUTOS] AS MOV ON (LINHA.COD_PRODUTO = MOV.COD_PRODUTO AND LINHA.COD_LOJA = MOV.COD_LOJA)
WHERE 1 = 1
AND LINHA.FORA_LINHA = 'S'
--AND LINHA.QTDE_PRODUTO_1ANO > 0;



