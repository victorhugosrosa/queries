Select      
	YEAR(DATA) AS ANO
	,MONTH(DATA) AS MES      
	--,TERMINAL
	,DESCRICAO
	,SUM(VALOR) VALOR
	,SUM(QTDE) QTDE
	
from [192.168.7.50,8513].Zeus_marche.dbo.ITENS_CUPONS
where 1 = 1
	AND CONVERT(date,data) >= CONVERT(date,'20131001')
	AND CONVERT(date,data) < CONVERT(date,'20140401')
	and PRODUTO in (102274,102275,102276)
	AND LOJ
GROUP BY
	YEAR(DATA)
	,MONTH(DATA)    
	--,TERMINAL
	,DESCRICAO
	

select
	ALTEC.DATA
	,isnull(PROD.PLU,999999) AS COD_PRODUTO
	,2 AS TIPO_VENDA 
	,SUM(QTDE) AS QTDE_PRODUTO
	,SUM(TOTAL) AS VALOR_TOTAL
	,0 AS VALOR_UNITARIO
	,COUNT(QTDE) AS QTDE_DEMANDA
	,0 AS COD_CLIENTE
from
	Alltec.dbo.ITENS_CUPONS as ALTEC left outer join Alltec.dbo.PRODUTO as PROD	on (PROD.CODPRODUTO = ALTEC.PRODUTO	AND PROD.COD_LOJA = ALTEC.COD_lOJA)
WHERE 1 = 1
	and ALTEC.DATA between convert(date,'20131001') and convert(date,'20140401')
	and ALTEC.PRODUTO in (102274,102275,102276)
GROUP BY
	ALTEC.DATA
	,PROD.PLU
	
