SELECT --TOP 100 
		SB.COD_LOJA
		,SB.CODIGO AS COD_PRODUTO
		,SB.DESCRICAO
		,SB.COD_SECAO
		,SB.COD_GRUPO
		,E.CLASSIF_PRODUTO_LOJA
		,SB.DIAS_NAO_VENDA
		,SB.DIAS_NAO_COMPRA
		,SB.DIAS_COM_ESTOQUE
		,SB.ESTOQUE
		,(CASE
			WHEN SB.DIAS_NAO_VENDA >= 0 AND SB.DIAS_NAO_VENDA <= 7 THEN '000-007'
			WHEN SB.DIAS_NAO_VENDA > 7 AND SB.DIAS_NAO_VENDA <= 14 THEN '008-014'
			WHEN SB.DIAS_NAO_VENDA > 14 AND SB.DIAS_NAO_VENDA <= 30 THEN '015-030'
			WHEN SB.DIAS_NAO_VENDA > 30 AND SB.DIAS_NAO_VENDA <= 60 THEN '031-060'
			WHEN SB.DIAS_NAO_VENDA > 60 AND SB.DIAS_NAO_VENDA <= 90 THEN '061-090'
			WHEN SB.DIAS_NAO_VENDA > 90 AND SB.DIAS_NAO_VENDA <= 120 THEN '091-120'
			WHEN SB.DIAS_NAO_VENDA > 120 AND SB.DIAS_NAO_VENDA <= 150 THEN '121-150'
			WHEN SB.DIAS_NAO_VENDA > 150 AND SB.DIAS_NAO_VENDA <= 180 THEN '151-180'
			WHEN SB.DIAS_NAO_VENDA > 180 AND SB.DIAS_NAO_VENDA <= 360 THEN '180-360'
			WHEN SB.DIAS_NAO_VENDA > 360 AND SB.DIAS_NAO_VENDA <= 720 THEN '360-720'			
			WHEN SB.DIAS_NAO_VENDA > 720 THEN '720-999'
			WHEN SB.DIAS_NAO_VENDA < 0 THEN '(-)'
		END) AS FAIXA_DNV
		,(CASE
			WHEN SB.DIAS_NAO_COMPRA >= 0 AND SB.DIAS_NAO_COMPRA <= 7 THEN '000-007'
			WHEN SB.DIAS_NAO_COMPRA > 7 AND SB.DIAS_NAO_COMPRA <= 14 THEN '008-014'
			WHEN SB.DIAS_NAO_COMPRA > 14 AND SB.DIAS_NAO_COMPRA <= 30 THEN '015-030'
			WHEN SB.DIAS_NAO_COMPRA > 30 AND SB.DIAS_NAO_COMPRA <= 60 THEN '031-060'
			WHEN SB.DIAS_NAO_COMPRA > 60 AND SB.DIAS_NAO_COMPRA <= 90 THEN '061-090'
			WHEN SB.DIAS_NAO_COMPRA > 90 AND SB.DIAS_NAO_COMPRA <= 120 THEN '091-120'
			WHEN SB.DIAS_NAO_COMPRA > 120 AND SB.DIAS_NAO_COMPRA <= 150 THEN '121-150'
			WHEN SB.DIAS_NAO_COMPRA > 150 AND SB.DIAS_NAO_COMPRA <= 180 THEN '151-180'
			WHEN SB.DIAS_NAO_COMPRA > 180 AND SB.DIAS_NAO_COMPRA <= 360 THEN '180-360'
			WHEN SB.DIAS_NAO_COMPRA > 360 AND SB.DIAS_NAO_COMPRA <= 720 THEN '360-720'			
			WHEN SB.DIAS_NAO_COMPRA > 720 THEN '720-999'
			WHEN SB.DIAS_NAO_COMPRA < 0 THEN '(-)'
		END) AS FAIXA_DNC
FROM
	BI.DBO.VW_BI_SUPERBASE AS SB INNER JOIN BI.DBO.COMPRAS_ESTATISTICA_PRODUTO AS E ON (E.COD_PRODUTO = SB.CODIGO AND E.COD_LOJA = SB.COD_LOJA)
		--INNER JOIN BI.DBO.BI_CAD_PRODUTO AS P ON (P.COD_PRODUTO = SB.CODIGO)
WHERE 1 = 1
	--AND P.PESADO = 'N'
	--AND SB.CODIGO = '00000208'
	--AND SB.COD_LOJA = 1
	
	