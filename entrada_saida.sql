select
	E.COD_LOJA
	--,MONTH(E.DTA_ENTRADA) AS DATA
	,E.COD_PRODUTO
	,CP.DESCRICAO
	,dbo.fn_FormataVlr_Excel(SUM(E.QTD_ENTRADA*E.QTD_EMBALAGEM)) AS QTD_ENTRADA
	--,dbo.fn_FormataVlr_Excel(SUM(VP.QTDE_PRODUTO)) AS QTD_VENDA
	,dbo.fn_FormataVlr_Excel((SELECT SUM(VP.QTDE_PRODUTO) FROM BI.dbo.BI_VENDA_PRODUTO AS VP WHERE CONVERT(DATE,VP.DATA) BETWEEN CONVERT(DATE,'20131001') AND CONVERT(DATE,'20131231') AND VP.COD_PRODUTO = E.COD_PRODUTO AND E.COD_LOJA = VP.COD_LOJA)) AS QTD_VENDA
from
	BI.dbo.BI_CAD_PRODUTO AS CP
		INNER JOIN [192.168.0.6].ZEUS_RTG.dbo.vw_MARCHE_ENTRADAS AS E ON (E.COD_PRODUTO = CP.COD_PRODUTO)
		--LEFT JOIN BI.dbo.BI_VENDA_PRODUTO AS VP ON (CP.COD_PRODUTO = VP.COD_PRODUTO AND E.COD_LOJA = VP.COD_LOJA)
WHERE 1 = 1
	AND CP.COD_PRODUTO IN
	(
	995473,
	995467,
	995469,
	995471,
	992906,
	992908,
	538947,
	541688,
	538909,
	541664,
	541695,
	541671,
	995148,
	995151,
	995146,
	995147,
	995145,
	995150,
	994386,
	994384,
	994385,
	994383
	)
	AND CONVERT(DATE,E.DTA_ENTRADA) BETWEEN CONVERT(DATE,'20131001') AND CONVERT(DATE,'20131231')
GROUP BY
	E.COD_LOJA
	--,MONTH(E.DTA_ENTRADA)
	,E.COD_PRODUTO
	,CP.DESCRICAO
ORDER BY
	E.COD_LOJA
	,E.COD_PRODUTO