SELECT DISTINCT
	right('00000000000000' + cast(T.COD_EAN as varchar(13)),13)
	,right('0000000000' + cast( T.COD_PRODUTO as varchar(10)),10) AS ITEMID
	,'AllBlank'	as [INVENTDIMID]
	,'EAN13' as [BARCODESETUPID]
	,0 
	,1
	,PAX.ITEMNAME
	,0
	,'11/25/2011'  
	,0
	,'vict1'
	,'vcm'
	,1
	,AX.UNITID as DES_UNIDADE_VENDA
	,1
	,0
	,NULL
from
	@TEMP_EAN as T INNER JOIN SMA_AX50_SP1_DB_PROD.dbo.INVENTTABLEMODULE as AX ON (AX.ITEMID  = right('0000000000' + cast(T.COD_PRODUTO as varchar(10)),10))
		INNER JOIN SMA_AX50_SP1_DB_PROD.dbo.INVENTTABLE AS PAX ON (AX.ITEMID  = PAX.ITEMID)
where 1 =1 
	and AX.MODULETYPE = 1
	and AX.DATAAREAID = 'BAR'
	and T.cod_produto <> '00000001'

